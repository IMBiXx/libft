#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned int	i;
	unsigned char	v;

	i = 0;
	v = '\0';
	if (n > 0)
		while (i < n)
			((unsigned char*)s)[i++] = v;
}
